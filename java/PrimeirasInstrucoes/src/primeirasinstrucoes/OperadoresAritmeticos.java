package primeirasinstrucoes;

public class OperadoresAritmeticos {

	public static void main(String[] args) {
		int a = 10, b = 25, c = 40, d = 55;
		
		System.out.println("Adição " + (a + b));
		System.out.println("Subtração " + (b -a));
		System.out.println("Multiplicação " + (c * d));
		System.out.println("Divisão " + (c / a));
		System.out.println("Modulo " + (d % a));

	}

}
